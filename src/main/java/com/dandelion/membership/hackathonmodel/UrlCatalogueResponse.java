package com.dandelion.membership.hackathonmodel;

import java.util.List;

public class UrlCatalogueResponse {
	private List<String> work;
	private List<String> home;
	private List<String> news;
	private List<String> social;
	private List<String> finance;
	private List<String> shopping;
	public List<String> getWork() {
		return work;
	}
	public void setWork(List<String> work) {
		this.work = work;
	}
	public List<String> getHome() {
		return home;
	}
	public void setHome(List<String> home) {
		this.home = home;
	}
	public List<String> getNews() {
		return news;
	}
	public void setNews(List<String> news) {
		this.news = news;
	}
	public List<String> getSocial() {
		return social;
	}
	public void setSocial(List<String> social) {
		this.social = social;
	}
	public List<String> getFinance() {
		return finance;
	}
	public void setFinance(List<String> finance) {
		this.finance = finance;
	}
	public List<String> getShopping() {
		return shopping;
	}
	public void setShopping(List<String> shopping) {
		this.shopping = shopping;
	}
	
	
}
